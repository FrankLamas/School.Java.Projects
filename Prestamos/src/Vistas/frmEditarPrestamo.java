    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (tidPrestamo.getText().equals("")) {
            return;
        }
        OpPrestamos op = new OpPrestamos();
        String datos[][] = op.buscar(0, tidPrestamo.getText());
        if (datos.length<1) {
            JOptionPane.showMessageDialog(this, "El prestamo no existe.");
            return;
        }
        tClave.setText(datos[0][4]);
        tUsuario.setText(datos[0][5]);
        cmbEstado.setSelectedIndex(datos[0][3].equals("Concluido")?0:datos[0][3].equals("Pendiente")?1:datos[0][3].equals("Cancelado")?2:0);
    }//GEN-LAST:event_jButton3ActionPerformed

