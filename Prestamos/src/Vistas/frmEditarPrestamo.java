    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tidPrestamo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No se puede realizar esta acción, porque el id prestamo está vacío.");
            return;
        }
        OpPrestamos op = new OpPrestamos();
        if (op.buscar(0, tidPrestamo.getText()).length<1) {
            JOptionPane.showMessageDialog(this, "No se puede realizar esta acción, porque el prestamo no existe.");
            return;
        }
        op.updateDato(Integer.parseInt(tidPrestamo.getText()), cmbEstado.getSelectedItem().toString());
        JOptionPane.showMessageDialog(this, "Operación realizada.");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

